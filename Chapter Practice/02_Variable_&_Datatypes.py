# Python Data Types Examples

# Integer (int) - whole numbers
age = 25
print("Integer:", age, type(age))

# Float - decimal numbers
height = 5.9
print("Float:", height, type(height))

# Complex - numbers with real and imaginary parts
complex_num = 3 + 4j
print("Complex:", complex_num, type(complex_num))

# String (str) - sequence of characters
name = "Alice"
print("String:", name, type(name))

# Bool - True or False
is_student = True
has_license = False
print("Boolean True:", is_student, type(is_student))
print("Boolean False:", has_license, type(has_license))

# None - represents absence of value
result = None
print("None:", result, type(result))

# Python Operators Examples

# 1. Arithmetic Operators
print("\n=== Arithmetic Operators ===")
a = 10
b = 3
print(f"Addition: {a} + {b} = {a + b}")        # Addition
print(f"Multiplication: {a} * {b} = {a * b}") # Multiplication

# 2. Comparison Operators
print("\n=== Comparison Operators ===")
x = 5
y = 10
print(f"{x} == {y}: {x == y}")  # Equal to
print(f"{x} < {y}: {x < y}")    # Less than

# 3. Logical Operators
print("\n=== Logical Operators ===")
p = True
q = False
print(f"{p} and {q}: {p and q}")  # Logical AND
print(f"{p} or {q}: {p or q}")    # Logical OR

# 4. Assignment Operators
print("\n=== Assignment Operators ===")
num = 10
print(f"Initial num = {num}")
num += 5   # num = num + 5
print(f"num += 5: {num}")
num *= 2   # num = num * 2
print(f"num *= 2: {num}")

